class PHPiano
{
	const PHPIANO_VERSION = "0.0";
	const RPC_URL = "http://www.pandora.com/radio/xmlrpc/v28?";
	const USER_AGENT = "PHPiano/0.0";
	const HTTP_TIMEOUT = 30;
	const AUDIO_FORMAT = 'aacplus';
	
	const RATE_BAN = 'ban';
	const RATE_LOVE = 'love';
	const RATE_NONE = null;
	
	const PLAYLIST_VALIDITY_TIME = 10800;	// 3 hours
	
	// We hang on to these so that we can re-auth later
	private $username;
	private $password;
	
	// Internal "guts"
	private $routeId;
	private $listenerId;
	private $authToken;
	
	// Disable logging by default
	var $loglevel = 0;
	
	private $xmlrpc_format_options = array(
			'output_type'=>'xml',
			'verbosity'=>'no_white_space', 
			'escaping'=>array('non-ascii', 'non-print', 'markup'),
			'version'=>'xmlrpc',
			'encoding'=>'utf8');
	
	// Our cURL handle
	private $ch;
	
	// Our Blowfish handle
	private $blowfish;
	
	// Our blowfish keys
	private $keys;
	
	function __construct($user=null, $pass=null)
	{
		$this->log("Instantiating ".self::USER_AGENT);
		
		// Check for dependencies
		if(!function_exists('xmlrpc_encode_request'))
			throw new PHPiano_Error('Need the XMLRPC extensions to PHP.');
		
		$this->make_routeId();
		// Note: These get set to null or the value, which is desired
		$this->set_username($user);
		$this->set_password($pass);
		
		// Setup the cURL handle
		$this->ch = curl_init();
		curl_setopt($this->ch, CURLOPT_HEADER, false);
		curl_setopt($this->ch, CURLOPT_CUSTOMREQUEST, 'POST');	// Custom so we use our own Content-Type header
		curl_setopt($this->ch, CURLOPT_RETURNTRANSFER, true);
		curl_setopt($this->ch, CURLOPT_TIMEOUT, self::HTTP_TIMEOUT);
		curl_setopt($this->ch, CURLOPT_CONNECTTIMEOUT, self::HTTP_TIMEOUT);
		curl_setopt($this->ch, CURLOPT_USERAGENT, self::USER_AGENT);
		
		// Grab our blowfish keys
		$this->keys = new Pandora_Keys();
		
		require_once('Crypt/Blowfish.php');
		$this->blowfish_enc = new Crypt_Blowfish(null);
		$this->blowfish_dec = new Crypt_Blowfish($this->keys->in_key_p);
	}
	
	// Simple accessor methods
	function make_routeId()
	{
		$this->routeId = sprintf("%07luP", time() % 10000000);
		$this->log("routeId = ".$this->routeId);
		// Also reset the authToken and listenerId
		$this->authToken=null;
		$this->listenerId=null;
	}
	
	function set_username($user=null)
	{
		if(is_null($user))
			$this->log(LOG_WARNING, "Setting username to a null value.");
		$this->username=$user;
		$this->log(LOG_WARNING, "username = ".$this->username);
		// Also reset the authToken and listenerId
		$this->authToken=null;
		$this->listenerId=null;
	}
	
	function set_password($pass=null)
	{
		if(is_null($pass))
			$this->log(LOG_WARNING, "Setting password to a null value.");
		$this->password=$pass;
		$this->log(LOG_WARNING, "password = ".$this->password);
		// Also reset the authToken and listenerId
		$this->authToken=null;
		$this->listenerId=null;
	}
	
	
	// The real meat of things, user methods
	function connect($user=null, $pass=null)
	{
		// No auth data provided, check that we already have it
		if(is_null($user) && is_null($pass))
		{
			if(is_null($this->username) && is_null($this->password))
				throw new PHPiano_Error('No username or password given.');
		} else
		{
			$this->set_username($user);
			$this->set_password($pass);
		}
		
		// Sanity check, we can encrypt/decrypt. misc.sync
		$xmlrpc = $this->xmlrpc_call('misc.sync');
		
		// Perform the actual authentication
		$xmlrpc = $this->xmlrpc_call('listener.authenticateListener', array($this->username, $this->password));
		
		// XXX Do something more ;)
		
	}
	
	
	// The internal "guts" methods
	private function xmlrpc_call($method, $args=array(), $url_args=true)
	{
		if($url_args)
			$url_args=$args;
		array_unshift($args, time());
		if($this->authToken)
			array_unshift($args, $this->authToken);
		
		$this->log("Making XMLRPC call '$method' with ".sizeof($args)." args: ".print_r($args, true));
		$xmlrpc = xmlrpc_encode_request($method, $args, $this->xmlrpc_format_options);
		$this->log("XMLRPC Call: $xmlrpc");
		$xmlrpc_enc = $this->pandora_encrypt($xmlrpc);
		
		$url_bits=array();
		if($this->routeId)
			$url_bits[] = 'rid='.$this->routeId;
		if($this->listenerId)
			$url_bits[] = 'lid='.$this->listenerId;
		// Figure out the method=
		$url_bits[] = 'method='.substr(strrchr($method, "."), 1);
		
		$xmlrpc_url = self::RPC_URL.implode($url_bits, '&');
		$this->log("Using url: $xmlrpc_url");
		
		$response = $this->curl_post($xmlrpc_url, $xmlrpc_enc);
		$this->log("Response received: $response");
		$response=xmlrpc_decode($response);
		$this->log("Decoded response: ".print_r($response, true));
		if(xmlrpc_is_fault($response))
		{
			throw new PHPiano_Error("Protocol error!\n{$response['faultCode']}: {$response['faultString']}");
		}
		
		return $response;
	}
	
	private function curl_post($url, $data)
	{
		$this->log("Posting ".strlen($data)." bytes to $url");
		curl_setopt($this->ch, CURLOPT_URL, $url);
		$headers = array(
			"Content-type: text/xml",
			"Content-length: ".strlen($data),
			"\r\n",
			);
		curl_setopt($this->ch, CURLOPT_HTTPHEADER, $headers);
		curl_setopt($this->ch, CURLOPT_POSTFIELDS, $data);
		$response = curl_exec($this->ch);	//XXX
		return $response;
	}
	
	// We encrypt all our requests to Pandora
	private function pandora_encrypt($text)
	{
		$this->log("Encrypting ".strlen($text)." bytes into Blowfish data.");
		
		$res = $this->blowfish_enc->setKey(hex2bin($this->keys->out_key_p));
		if (PEAR::isError($res))
			throw new PHPiano_Error('Error with key: '.$res->getMessage());
		
		$encrypted = $this->blowfish_enc->encrypt($text);
		if (PEAR::isError($encrypted))
			throw new PHPiano_Error('Error while encrypting: '.$encrypted->getMessage());
		
		$encrypted = bin2hex($encrypted);
		
		$this->log("Returning ".strlen($encrypted)." bytes of Blowfish data.");
		return $encrypted;
	}
	
	// Only used with the song url
	private function pandora_decrypt($text)
	{
		$this->log("Decrypting ".strlen($text)." bytes of Blowfish data.");
		
		$res = $this->blowfish_dec->setKey(hex2bin($this->keys->in_key_p));
		if (PEAR::isError($res))
			throw new PHPiano_Error('Error with key: '.$res->getMessage());
		
		$decrypted = $this->blowfish_enc->decrypt($text);
		if (PEAR::isError($decrypted))
			throw new PHPiano_Error('Error while encrypting: '.$decrypted->getMessage());
		
		$decrypted = bin2hex($decrypted);
		
		$this->log("Returning ".strlen($decrypted)." bytes of decrypted data.");
		return $decrypted;
	}
	
	private function log($arg1, $arg2=null)
	{
		// Overcome PHP's limited overriding abilities
		if(is_null($arg2))
		{
			$message=$arg1;
			$level=LOG_DEBUG;
		} else
		{
			$message=$arg2;
			$level=$arg1;
		}
		if(true)	// XXX add the $level logic
		{
			$pfxstring = date('[Y-m-d H:i:s]')." ";
			fwrite(STDERR, $pfxstring.trim($message).PHP_EOL);
			fflush(STDERR);
		}
	}
}

